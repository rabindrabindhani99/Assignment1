public class OperatorEx
{
    public static void main(String[] args)
    {
        int a=16;
        int b=5;
        boolean c=true;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a<<5);
        System.out.println(a>>2);
        System.out.println(~a);
        System.out.println(!c);
    }
}
